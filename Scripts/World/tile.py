class Tile():

	def __init__(self, index, X, Y, rules = None,tiletype=0, Priority = 1):
		self.index = index
		name ="row-" + str(X)  + "-column-" + str(Y) + ".jpeg"
		self.image = name
		self.rules = rules
		self.tiletype = tiletype
		self.Priority = Priority
		
		
		if rules is None:
			self.rules =  {"Up":[], "Dn": [], "Lf": [], "Rt": []}

		for dir in ["Up", "Dn", "Lf", "Rt"]:
			if dir not in self.rules:
				self.rules[dir] = []
		
		if "All" in self.rules:
			for rule in self.rules["All"]:
				self.rules["Up"].append(rule)
				self.rules["Dn"].append(rule)
				self.rules["Lf"].append(rule)
				self.rules["Rt"].append(rule)
			del self.rules["All"]


